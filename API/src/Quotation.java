public class Quotation {
    public String name;
    public String high;
    public String low;
    public String bid;

    public String getName() {
        return name;
    }
    public String getHigh() {
        return high;
    }
    public String getLow() {
        return low;
    }
    public String getBid() {
        return bid;
    }
    
}
